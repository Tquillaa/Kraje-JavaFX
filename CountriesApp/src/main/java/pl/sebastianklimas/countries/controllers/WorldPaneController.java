    private void setNewColorToSpecifiedCountry (SVGPath svgPath, NewColor color) {
        switch (color) {
            case BLACK -> {
                painBlack(svgPath);
                setStroke(svgPath, 1, Color.WHITE);
                break;
            }
            case RED -> {
                paintRed(svgPath);
                setStroke(svgPath, 2, Color.BLACK);
                break;
            }
            case GREEN -> {
                paintGreen(svgPath);
                setStroke(svgPath, 0, Color.GREEN);
                break;
            }
        }
    }

    private void painBlack(SVGPath svgPath) {
        svgPath.setFill(Color.BLACK);
    }
    private void paintGreen(SVGPath svgPath) {
        svgPath.setFill(Color.GREEN);
    }

    private void paintRed(SVGPath svgPath) {
        svgPath.setFill(Color.RED);
    }

    private void setStroke(SVGPath svgPath , double size, Color color) {
        svgPath.setStrokeWidth(size);
        svgPath.setStroke(color);
    }

